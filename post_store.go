func (db *Store) Post(ctx context.Context, in RetrievePost) (Post, error) {
	const query = `
		SELECT
			  posts.id
			, posts.user_id
			, posts.content
			, posts.reactions_count
			, post_reactions.reactions
			, posts.comments_count
			, posts.created_at
			, posts.updated_at
			, users.username
			, users.avatar_path
			, users.avatar_width
			, users.avatar_height
		FROM posts
		INNER JOIN users ON posts.user_id = users.id
		LEFT JOIN (
			SELECT post_id, array_agg(reaction) AS reactions
			FROM post_reactions
			WHERE post_reactions.user_id = $1
			GROUP BY post_id
		) AS post_reactions ON post_reactions.post_id = posts.id
		WHERE posts.id = $2
	`
	var p Post
	var reactions []string
	err := db.QueryRowContext(ctx, query, in.authUserID, in.ID).Scan(
		&p.ID,
		&p.UserID,
		&p.Content,
		&jsonValue{Dst: &p.ReactionsCount},
		pq.Array(&reactions),
		&p.CommentsCount,
		&p.CreatedAt,
		&p.UpdatedAt,
		&p.User.Username,
		db.AvatarScanFunc(&p.User.AvatarPath),
		&p.User.AvatarWidth,
		&p.User.AvatarHeight,
	)
	if errors.Is(err, sql.ErrNoRows) {
		return Post{}, ErrPostNotFound
	}

	if err != nil {
		return Post{}, fmt.Errorf("sql select post: %w", err)
	}

	p.ReactionsCount.Apply(reactions)

	return p, nil
}

func (db *Store) PostReactionsCount(ctx context.Context, postID string) (ReactionsCount, error) {
	const query = `
		SELECT reactions_count FROM posts WHERE id = $1
	`

	var out ReactionsCount
	row := db.QueryRowContext(ctx, query, postID)
	err := row.Scan(&jsonValue{Dst: &out})
	if errors.Is(err, sql.ErrNoRows) {
		return nil, ErrPostNotFound
	}

	if err != nil {
		return nil, fmt.Errorf("sql select post reactions count: %w", err)
	}

	return out, nil
}

func (db *Store) UpdatePost(ctx context.Context, in UpdatePost) (time.Time, error) {
	const query = `
		UPDATE posts
		SET comments_count = comments_count + $1
			, reactions_count = COALESCE($2, reactions_count)
			, updated_at = now()
		WHERE id = $3
		RETURNING updated_at
	`
	var updatedAt time.Time
	row := db.QueryRowContext(ctx, query,
		in.IncreaseCommentsCountBy,
		jsonValue{Dst: in.ReactionsCount},
		in.PostID,
	)
	err := row.Scan(&updatedAt)
	if err != nil {
		return time.Time{}, fmt.Errorf("sql update post: %w", err)
	}

	return updatedAt, nil
}
